let day = prompt ('Introduce un número del 1 al 7');

switch (+day) {
    case 1:
        alert ('El numero corresponde a lunes'); 
        break;
    case 2:
        alert ('El numero corresponde a martes');
        break;
    case 3:
        alert ('El numero corresponde a miercoles');
        break;
    case 4:
        alert ('El numero corresponde a jueves');
        break;
    case 5:
        alert ('El numero corresponde a viernes');
        break;
    case 6:
        alert ('El numero corresponde a sábado')
        break;
    case 7:
        alert ('El numero corresponde a domingo');
        break;
    default:
        alert ('Introduce un número válido')
}






