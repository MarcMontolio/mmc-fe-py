let month = prompt ('Introduce un número del 1 al 12');

switch (+month) {
    case 1:
        alert ('El numero corresponde a enero'); 
        break;
    case 2:
        alert ('El numero corresponde a febrero');
        break;
    case 3:
        alert ('El numero corresponde a marzo');
        break;
    case 4:
        alert ('El numero corresponde a abril');
        break;
    case 5:
        alert ('El numero corresponde a mayo');
        break;
    case 6:
        alert ('El numero corresponde a junio')
        break;
    case 7:
        alert ('El numero corresponde a julio');
        break;
    case 8:
        alert ('El numero corresponde a agosto');
        break;
    case 9:
        alert ('El numero corresponde a septiembre');
        break;
    case 10:
        alert ('El numero corresponde a octubre');
        break;
    case 11:
        alert ('El numero corresponde a noviebre');
        break;
    case 12:
        alert ('El numero corresponde a diciembre');
        break;
    default:
        alert ('Introduce un número válido')
}